<template>
  <div>

    <slot name="header"></slot>

    <ms-node-tree
      v-loading="result.loading"
      :tree-nodes="data"
      :type="isReadOnly ? 'view' : 'edit'"
      @add="add"
      @edit="edit"
      @drag="drag"
      @remove="remove"
      @refresh="list"
      @nodeSelectEvent="nodeChange"
      ref="nodeTree">

      <template v-slot:header>
        <el-input :placeholder="$t('test_track.module.search')" v-model="condition.filterText" size="small">
          <template v-slot:append>
            <el-dropdown v-if="!isReadOnly" size="small" split-button type="primary" class="ms-api-button" @click="handleCommand('add-api')"
                         v-tester
                         @command="handleCommand" trigger="click">
              <el-button icon="el-icon-folder-add" @click="addScenario"></el-button>
              <el-dropdown-menu slot="dropdown">
                <el-dropdown-item command="add-scenario">{{ $t('api_test.automation.add_scenario') }}</el-dropdown-item>
                <el-dropdown-item command="import">{{ $t('api_test.api_import.label') }}</el-dropdown-item>
                <el-dropdown-item command="exports">
                  <el-dropdown placement="right-start" @command="chooseExportType">
                <span>
                  {{ $t('report.export') }} <i class="el-icon-arrow-down el-icon--right"></i>
                </span>
                    <el-dropdown-menu slot="dropdown">
                      <template>
                        <el-dropdown-item command="export">{{ $t('report.export_to_ms_format') }}</el-dropdown-item>
                        <el-dropdown-item command="exportJmx">{{ $t('report.export') }} JMETER 格式</el-dropdown-item>
                      </template>
                    </el-dropdown-menu>
                  </el-dropdown>
                </el-dropdown-item>
              </el-dropdown-menu>
            </el-dropdown>
          </template>
        </el-input>
        <module-trash-button v-if="!isReadOnly" :condition="condition" :exe="enableTrash"/>
      </template>

    </ms-node-tree>

    <ms-add-basis-scenario
      @saveAsEdit="saveAsEdit"
      @refresh="refresh"
      ref="basisScenario"/>

    <api-import ref="apiImport" :moduleOptions="moduleOptions" @refreshAll="$emit('refreshAll')"/>
  </div>

</template>

<script>
  import SelectMenu from "../../../track/common/SelectMenu";
  import MsAddBasisScenario from "@/business/components/api/automation/scenario/AddBasisScenario";
  import MsNodeTree from "../../../track/common/NodeTree";
  import {buildNodePath} from "../../definition/model/NodeTree";
  import ModuleTrashButton from "../../definition/components/module/ModuleTrashButton";
  import ApiImport from "./common/ScenarioImport";

  export default {
    name: 'MsApiScenarioModule',
    components: {
      ApiImport,
      ModuleTrashButton,
      MsNodeTree,
      MsAddBasisScenario,
      SelectMenu,
    },
    props: {
      isReadOnly: {
        type: Boolean,
        default() {
          return false
        }
      },
      relevanceProjectId: String,
      planId: String
    },
    computed: {
      isPlanModel() {
        return this.planId ? true : false;
      },
      isRelevanceModel() {
        return this.relevanceProjectId ? true : false;
      },
      projectId() {
        return this.$store.state.projectId
      },
    },
    data() {
      return {
        result: {},
        condition: {
          filterText: "",
          trashEnable: false
        },
        data: [],
        currentModule: undefined,
        moduleOptions: [],
      }
    },
    mounted() {
      this.list();
    },
    watch: {
      'condition.filterText'(val) {
        this.$refs.nodeTree.filter(val);
      },
      'condition.trashEnable'() {
        this.$emit('enableTrash', this.condition.trashEnable);
      },
      planId() {
        this.list();
      },
      relevanceProjectId() {
        this.list();
      }
    },
    methods: {
      handleCommand(e) {
        switch (e) {
          case "add-scenario":
            this.addScenario();
            break;
          case "import":
            this.result = this.$get("/api/automation/module/list/" + this.projectId, response => {
              if (response.data != undefined && response.data != null) {
                this.data = response.data;
                let moduleOptions = [];
                this.data.forEach(node => {
                  buildNodePath(node, {path: ''}, moduleOptions);
                });
                this.moduleOptions = moduleOptions
              }
            });
            this.$refs.apiImport.open(this.currentModule);
            break;
          case "export":
            this.$emit('exportAPI');
            break;
          case "exportJmx":
            this.$emit('exportJmx');
            break;
        }
      },
      chooseExportType(e) {
        switch (e) {
          case "export":
            this.$emit('exportAPI');
            break;
          case "exportJmx":
            this.$emit('exportJmx');
            break;
        }
      },
      list(projectId) {
        let url = undefined;
        if (this.isPlanModel) {
          url = '/api/automation/module/list/plan/' + this.planId;
        } else if (this.isRelevanceModel) {
          url = "/api/automation/module/list/" + this.relevanceProjectId;
        } else {
          url = "/api/automation/module/list/" + (projectId ? projectId : this.projectId);
          if (!this.projectId) {
            return;
          }
        }
        this.result = this.$get(url, response => {
          if (response.data != undefined && response.data != null) {
            this.data = response.data;
            let moduleOptions = [];
            this.data.forEach(node => {
              buildNodePath(node, {path: ''}, moduleOptions);
            });
            this.$emit('setModuleOptions', moduleOptions);
            this.$emit('setNodeTree', this.data);
            if (this.$refs.nodeTree) {
              this.$refs.nodeTree.filter(this.condition.filterText);
            }
          }
        });
      },
      edit(param) {
        param.projectId = this.projectId;
        param.protocol = this.condition.protocol;
        this.$post("/api/automation/module/edit", param, () => {
          this.$success(this.$t('commons.save_success'));
          this.list();
          this.refresh();
        }, (error) => {
          this.list();
        });
      },
      add(param) {
        param.projectId = this.projectId;
        param.protocol = this.condition.protocol;
        this.$post("/api/automation/module/add", param, () => {
          this.$success(this.$t('commons.save_success'));
          this.list();
        }, (error) => {
          this.list();
        });

      },
      remove(nodeIds) {
        this.$post("/api/automation/module/delete", nodeIds, () => {
          this.list();
          this.refresh();
        }, (error) => {
          this.list();
        });
      },
      drag(param, list) {
        this.$post("/api/automation/module/drag", param, () => {
          this.$post("/api/automation/module/pos", list, () => {
            this.list();
          });
        }, (error) => {
          this.list();
        });
      },
      nodeChange(node, nodeIds, pNodes) {
        this.currentModule = node.data;
        this.condition.trashEnable = false;
        if (node.data.id === 'root') {
          this.$emit("nodeSelectEvent", node, [], pNodes);
        } else {
          this.$emit("nodeSelectEvent", node, nodeIds, pNodes);
        }
      },
      exportAPI() {
        this.$emit('exportAPI');
      },
      // debug() {
      //   this.$emit('debug');
      // },
      saveAsEdit(data) {
        this.$emit('saveAsEdit', data);
      },
      refresh() {
        this.$emit("refreshTable");
      },
      addScenario() {
        if (!this.projectId) {
          this.$warning(this.$t('commons.check_project_tip'));
          return;
        }
        this.$refs.basisScenario.open(this.currentModule);
      },
      enableTrash() {
        this.condition.trashEnable = true;
      }
    }
  }
</script>

